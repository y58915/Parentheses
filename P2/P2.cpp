# include<iostream>
using namespace std;

int n;
char parenthesis[10000];

void print(int n, int pos, int left, int right)
{
	if (right == n)
	{
		cout << parenthesis << endl;
	}
	else
	{
		if (left > right)
		{
			parenthesis[pos] = ')';
			print(n, pos + 1, left, right + 1);
		}

		if (left < n)
		{
			parenthesis[pos] = '(';
			print(n, pos + 1, left + 1, right);
		}
	}
}

int main()
{
	cin >> n;
	print(n, 0, 0, 0);
}

